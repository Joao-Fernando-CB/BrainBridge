<script>

let cart = [];
const prices = {
    'NR 01': 299.90,
    'NR 02': 249.90,
    'NR 03': 279.90,
    'NR 04': 299.90
};

function adicionarCarrinho(curso) {
    cart.push(curso);
    updateCart();
}

function updateCart() {
    const cartElement = document.getElementById('carrinho');
    const totalElement = document.getElementById('totalPrice');
    
    if (cart.length === 0) {
        cartElement.innerHTML = '<p class="text-gray-500 text-center italic">Seu carrinho está vazio</p>';
        totalElement.textContent = 'R$ 0,00';
        return;
    }
    
    let html = '';
    let total = 0;
    
    cart.forEach((curso, index) => {
        const price = prices[curso];
        total += price;
        html += `
            <div class="flex justify-between items-center p-2 ${index !== 0 ? 'border-t' : ''}">
                <div>
                    <p class="font-medium">${curso}</p>
                    <p class="text-sm text-gray-500">Curso Online</p>
                </div>
                <div class="flex items-center">
                    <span class="text-blue-600 font-medium mr-3">R$ ${price.toFixed(2)}</span>
                    <button onclick="removeFromCart(${index})" class="text-red-500 hover:text-red-700">
                        <svg xmlns="http://www.w3.org/2000/svg" class="h-5 w-5" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                            <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M19 7l-.867 12.142A2 2 0 0116.138 21H7.862a2 2 0 01-1.995-1.858L5 7m5 4v6m4-6v6m1-10V4a1 1 0 00-1-1h-4a1 1 0 00-1 1v3M4 7h16" />
                        </svg>
                    </button>
                </div>
            </div>
        `;
    });
    
    cartElement.innerHTML = html;
    totalElement.textContent = `R$ ${total.toFixed(2)}`;
}

function removeFromCart(index) {
    cart.splice(index, 1);
    updateCart();
}

function finalizarCompra() {
    if (cart.length === 0) {
        alert('Seu carrinho está vazio!');
        return;
    }
    
    alert('Compra finalizada com sucesso! Obrigado pela preferência.');
    cart = [];
    updateCart();
}

</script>